USE sejong_restaurant;

CREATE TABLE restaurant (
  id               INT                NOT NULL AUTO_INCREMENT,
  name             VARCHAR(100)       NOT NULL,
  category         VARCHAR(20)        NULL,
  description      TEXT               NULL,
  address          VARCHAR(255)       NULL,
  phone            VARCHAR(30)        NULL,
  open_time        VARCHAR(100)       NULL,
  main_image_url   VARCHAR(255)       NULL,
  PRIMARY KEY (id)
) ;

-- 4) menu 테이블
CREATE TABLE menu (
  id               INT                NOT NULL AUTO_INCREMENT,
  restaurant_id    INT                NOT NULL,
  name             VARCHAR(100)       NOT NULL,
  price            INT                NULL,
  description      TEXT               NULL,
  image_url        VARCHAR(255)       NULL,
  PRIMARY KEY (id),
  KEY idx_menu_restaurant (restaurant_id),
  CONSTRAINT fk_menu_restaurant
    FOREIGN KEY (restaurant_id)
    REFERENCES restaurant(id)
    ON DELETE CASCADE
    ON UPDATE CASCADE
);

CREATE TABLE review (
  id            INT              NOT NULL AUTO_INCREMENT,
  restaurant_id INT              NOT NULL,
  rating        DECIMAL(3,2)     NOT NULL COMMENT '1.00~5.00',
  comment       TEXT             NULL,
  created_at    DATE             NOT NULL,
  PRIMARY KEY (id),
  KEY idx_review_restaurant (restaurant_id),
  CONSTRAINT fk_review_restaurant
    FOREIGN KEY (restaurant_id)
    REFERENCES restaurant(id)
    ON DELETE CASCADE
    ON UPDATE CASCADE
);

